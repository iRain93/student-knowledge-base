// ============== 主程序 ==============

function main() {
  let createdDirs = 0;
  let createdFiles = 0;

  for (const [stage, grades] of Object.entries(curriculum)) {
    for (const [gradeKey, gradeData] of Object.entries(grades)) {
      for (const [subjectKey, subjectData] of Object.entries(gradeData.subjects)) {
        if (subjectData.desc && subjectData.desc.includes('已有内容')) continue;

        const subjectDir = path.join(docsDir, stage, gradeKey, subjectKey);
        if (!fs.existsSync(subjectDir)) {
          fs.mkdirSync(subjectDir, { recursive: true });
          createdDirs++;
        }

        // 生成知识结构.md
        const structureContent = generateKnowledgeStructure(stage, gradeKey, gradeData, subjectKey, subjectData);
        const structurePath = path.join(subjectDir, '知识结构.md');
        fs.writeFileSync(structurePath, structureContent, 'utf-8');
        createdFiles++;

        // 生成知识点.md
        for (const kp of (subjectData.kps || [])) {
          const kpContent = generateKnowledgePoint(gradeData.name, subjectData.name, kp);
          const safeName = kp.name.replace(/[\/\\:*?"<>|]/g, '-');
          const kpPath = path.join(subjectDir, `${safeName}.md`);
          fs.writeFileSync(kpPath, kpContent, 'utf-8');
          createdFiles++;
        }
      }
    }
  }

  console.log(`Done! Created ${createdDirs} directories, ${createdFiles} files.`);
}

main();