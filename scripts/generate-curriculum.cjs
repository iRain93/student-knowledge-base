// ============== 模板函数 ==============

function generateKnowledgeStructure(stage, gradeKey, gradeData, subjectKey, subjectData) {
  const title = `${gradeData.name}${subjectData.name}知识结构`;
  const tree = subjectData.tree || [];
  const kps = subjectData.kps || [];

  // 构建 mermaid 图
  let mermaid = `graph TD\n    A[${gradeData.name}${subjectData.name}]`;
  if (tree.length > 0) {
    const categories = [...new Set(tree.map(t => t[1]))];
    categories.forEach((cat, i) => {
      mermaid += ` --> B${i + 1}[${cat}]`;
    });
    let nodeIdx = categories.length + 2;
    const seen = new Set();
    categories.forEach((cat, i) => {
      tree.filter(t => t[1] === cat).forEach(t => {
        if (!seen.has(t[0])) {
          seen.add(t[0]);
          mermaid += `\n    B${i + 1} --> C${nodeIdx}[${t[0]}]`;
          nodeIdx++;
        }
      });
    });
  }

  // 构建知识点列表
  let kpTable = '| 序号 | 知识点 | 核心目标 |\n|------|--------|---------|\n';
  kps.forEach((kp, i) => {
    const safeLink = kp.name.replace(/[\/\\:*?"<>|]/g, '-');
    kpTable += `| ${i + 1} | [${kp.name}](./${safeLink}) | ${kp.goal} |\n`;
  });

  // 构建学习目标
  let goals = kps.map(kp => `- ${kp.goal}`).join('\n');

  return `# ${title}

## 知识体系总览

\`\`\`mermaid
${mermaid}
\`\`\`

## 知识点列表

${kpTable}

## 学习目标

${goals}
`;
}

